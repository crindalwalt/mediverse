                        Detailed Description
                    </h2>
                    <div class="section-content">
                        <h3>How It Works</h3>
                        <p>Ibuprofen belongs to a class of drugs called NSAIDs (nonsteroidal anti-inflammatory drugs). It works by blocking the production of certain natural substances that cause inflammation. This effect helps to decrease swelling, pain, or fever.</p>

                        <h3>Common Uses</h3>
                        <ul>
                            <li>Relief of mild to moderate pain</li>
                            <li>Reduction of fever</li>
                            <li>Treatment of inflammation from arthritis</li>
                            <li>Relief from headaches and migraines</li>
                            <li>Menstrual cramp relief</li>
                            <li>Dental pain management</li>
                            <li>Minor injuries and muscle aches</li>
                        </ul>

                        <h3>Dosage Guidelines</h3>
                        <p>The typical adult dose is 200-400mg taken every 4-6 hours as needed. The maximum daily dose should not exceed 1200mg for over-the-counter use. Always follow the directions on the product label or as directed by your healthcare provider.</p>

